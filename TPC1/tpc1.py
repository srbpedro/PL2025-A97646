import re, sys

src = sys.stdin.read()

####################################################################################
####################################################################################

sumoff = r'\s+?(?i:off)' + r'(.|\s)*?' + r'\W+(?i:on)'

src = re.sub(sumoff, '', src)
print(src)

####################################################################################
####################################################################################

pattern = rf"(?P<numbers>\d+(\.\d+)?)|(?P<sumprint>=)"

####################################################################################
####################################################################################

sum = 0.0
matches = list(re.finditer(pattern, src))
for match in matches:
    if match.group("numbers"):
        sum += float(match.group("numbers"))
    elif match.group("sumprint"):
        print("Soma at√© agora:", sum)
